create table COMMENTS
(
	ID INT,
	NAME VARCHAR(100),
	EMAIL VARCHAR(100),
	CONTENT TEXT,
	CREATED_ON TIMESTAMP,
	UPDATED_ON TIMESTAMP,
	POST_ID INT
);

create table POSTS
(
	ID INT,
	TITLE VARCHAR(100),
	CONTENT VARCHAR(200),
	CREATED_ON TIMESTAMP,
	UPDATED_ON TIMESTAMP
);

create table USERS
(
	ID INT,
	NAME VARCHAR(100),
	EMAIL VARCHAR(100),
	PASSWORD VARCHAR(50)
);